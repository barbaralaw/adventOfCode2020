
function runProgram(program) {
  let accumulator = 0;
  let visitedOperands = [0];
  let i = 0;
  while (i < program.length) {
    let curOperation = program[i].operation;
    let curArgument = program[i].argument;
    if (curOperation === "acc") {
      accumulator += curArgument;
      i++;
    } else if (curOperation === "jmp") {
      i += curArgument;
    } else if (curOperation === "nop") {
      i++;
    }
    if (visitedOperands.includes(i)) {
      let infiniteLoop = true;
      return [infiniteLoop, accumulator];
      //return accumulator;
    }
    visitedOperands.push(i);
  }
  infiniteLoop = false;
  return [infiniteLoop, accumulator];
}


function fixCode(program) {
  let j = 0;
  let ret = runProgram(program);
  let curOperation;
  const programCopy = JSON.parse(JSON.stringify(program));
  while (j < program.length) {
    program = JSON.parse(JSON.stringify(programCopy));
    if (ret[0] === true) {
      curOperation = program[j].operation;
      if (curOperation === "jmp") {
        program[j].operation  = "nop";
        j++;
        ret = runProgram(program);
      } else if (curOperation === "nop") {
        program[j].operation = "jmp";
        j++;
        ret = runProgram(program);
      } else {
        j++;
      }
    } else {
      console.log(ret[1]);
      break;
    }
  }
}


